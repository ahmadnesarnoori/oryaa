  <h1 class="border-bottom">Double-Entry Accounting Scalability to the Current Technology</h1>
  <P>6 Pages &nbsp;&nbsp; Posted: 31-Aug-2018</p>
  <p><b>Author: <a href="nesar-ahmad-noori">Nesar Ahmad Noori</a></b></p>
  <p>This paper discusses the scalability of the double-entry accounting system to the current technology growth. Technology is one of the main improvement factors in the accounting industry, but unfortunately, camper to communication, social media, entertainment, and other industries it's not well enough adopted in the accounting industry, which is mostly because of the weakness which double-entry accounting has. First, the double-entry accounting system suffers from the time gap problem between doing and recording transactions, Second, this system ignores billions of individual access to essential financial services. Third, it is a kludge of a paper-based process and industrial technology. The best solution to these problems has emerged: peer-to-peer electronic accounting. It is a developed system of double-entry accounting, but instead of concentrating on one party it views and records a transaction for both affected sides at the same time.</p>
